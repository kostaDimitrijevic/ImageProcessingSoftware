package projekat;

public class Pixel {
	int red;
	int green;
	int blue;
	int alpha;
	public Pixel(int red_, int green_, int blue_, int alpha_) {
		red = red_;
		green = green_;
		blue = blue_;
		alpha = alpha_;
	}
	public int getRed() {
		return red;
	}
	public void setRed(int red) {
		this.red = red;
	}
	public int getGreen() {
		return green;
	}
	public void setGreen(int green) {
		this.green = green;
	}
	public int getBlue() {
		return blue;
	}
	public void setBlue(int blue) {
		this.blue = blue;
	}
	public int getAlpha() {
		return alpha;
	}
	public void setAlpha(int alpha) {
		this.alpha = alpha;
	}
	
	
}
